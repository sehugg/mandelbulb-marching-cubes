//the chunk class
export class Chunk {
    x;
    y;
    z;
    resolution;
    cellNumber;
    cellSize;
    cutOff;
    chunk: number[][][];
    triangles: number[];
    vertices: number[];
    indices: number[];
    normals: number[];
    vertexChecker: { [key: string]: number };

    constructor(x: number, y: number, z: number,
        resolution: number,
        readonly chunkWidth: number,
        readonly world: WorldShape)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.resolution = resolution;
        this.cellNumber = chunkWidth * (1 / this.resolution);
        this.cellSize = chunkWidth / this.cellNumber;

        this.cutOff = 0;

        this.chunk = [];

        this.triangles = [];
        this.vertices = [];
        this.indices = [];
        this.normals = [];

        this.vertexChecker = {};
    }


    //initializes block data
    create() {
        var cs = this.cellSize;
        var w = this.cellNumber + 1; //its +1 because we're dealing with triangles not squares

        //generates a density value for each cell in a chunk
        for (var x = w; x--;) {
            this.chunk[x] = [];
            for (var y = w; y--;) {
                this.chunk[x][y] = [];
                for (var z = w; z--;) {
                    //im storing values in a dictionary type array because it will prevent a lot of for loops in the future
                    let density = this.world.getDensity(this.x + (x * cs), this.y + (y * cs), this.z + (z * cs));
                    /*
                    density += this.world.getDensity(this.x + ((x+0.5) * cs), this.y + (y * cs), this.z + (z * cs));
                    density += this.world.getDensity(this.x + (x * cs), this.y + ((y+0.5) * cs), this.z + (z * cs));
                    density += this.world.getDensity(this.x + (x * cs), this.y + (y * cs), this.z + ((z+0.5) * cs));
                    density *= 0.25;
                    */
                    this.chunk[x][y][z] = density;
                }
            }
        }
        // TODO: figure out if empty at this point, maybe only test edges

        //this builds and can rebuild the mesh anytime
        this.createMeshData();
    }

    buildMesh(scene: BABYLON.Scene) {
        if (this.vertices.length == 0) {
            return null;
        }
        
        var mat = new BABYLON.StandardMaterial("mat", scene);
        mat.backFaceCulling = true;
    
        let key = this.x + " " + this.y + " " + this.z + " " + this.chunkWidth;
        let mesh = new BABYLON.Mesh(key, scene);
        mesh.material = mat;
    
        var vertexData = new BABYLON.VertexData();
        vertexData.positions = this.vertices;
        vertexData.indices = this.indices;
        vertexData.normals = this.normals;
        vertexData.applyToMesh(mesh, true);

        //this just sets the mesh position to its right place        
        /*
        mesh.position = new BABYLON.Vector3(
            (this.x + this.chunkWidth / 4) * chunkSizeMultiplier,
            (this.y + this.chunkWidth / 4) * chunkSizeMultiplier,
            (this.z + this.chunkWidth / 4) * chunkSizeMultiplier
        );
        */
        mesh.position = new BABYLON.Vector3(this.x, this.y, this.z);
        if (smoothShaded) {
            mesh.optimizeIndices();
        }
        
        return mesh;
    }

    //initializes mesh data
    createMeshData() {
        var cs = this.cellSize * chunkSizeMultiplier;
        var w = this.cellNumber;
        const thresh = densityThreshold * cs;
        //console.log(cs, thresh);

        for (var z = w; z--;) {
            for (var y = w; y--;) {
                for (var x = w; x--;) {
                    var cube = [];
                    var config = 0;

                    //gets configuration for the 8 corners of a cube
                    for (var i = 0; i < 8; i++) {
                        var corner = new BABYLON.Vector3(x, y, z).add(cornerTable[i]);
                        cube[i] = this.chunk[corner.x][corner.y][corner.z];

                        //the actual magic behind the entire algorithm
                        // a left shift for corner configuration
                        // the density function defines how mush of a mesh is inside a cell
                        // if the value is greater than zero then there is mesh data contained in that cell
                        // so it transforms corners to binary data to get the mesh configuration
                        // which is basically just filling up remaining zeros
                        if (cube[i] >= densityThreshold) {
                            config |= 1 << i;
                        }
                    }

                    this.marchCube(x, y, z, cs, cube, config);
                }
            }
        }
        console.log('verts', this.vertices.length, 'edges', this.indices.length, 'tris', this.triangles.length);
    }

    //flat normals
    computeFlatNormals(v1: BABYLON.Vector3, v2: BABYLON.Vector3, v3: BABYLON.Vector3) {
        var u = v2.subtract(v1);
        var v = v3.subtract(v1);

        for (var i = 0; i < 3; i++) {
            var n = new BABYLON.Vector3(
                (u.y * v.z) - (u.z * v.y),
                (u.z * v.x) - (u.x * v.z),
                (u.x * v.y) - (u.y * v.x)
            );

            this.normals.push(n.x);
            this.normals.push(n.y);
            this.normals.push(n.z);
        }
    }

    //smooth normals 
    // works with smooth meshes not rough
    // Please use BABYLON's normals computation if you want smooth normals on rough meshes
    computeSmoothNormals(x: number, y: number, z: number) {
        var v = this.chunk[x][y][z];
        var dx = v - this.chunk[x + 1][y][z];
        var dy = v - this.chunk[x][y + 1][z];
        var dz = v - this.chunk[x][y][z + 1];

        var n = BABYLON.Vector3.Normalize(new BABYLON.Vector3(dx, dy, dz));
        this.normals.push(n.x);
        this.normals.push(n.y);
        this.normals.push(n.z);
    }

    computeCubeNormals(cube: number[]) {
        // compute normal from densities of 8 corners of cube
        var dx = cube[0] - cube[1] + cube[2] - cube[3] + cube[4] - cube[5] + cube[6] - cube[7];
        var dy = cube[0] - cube[1] - cube[2] + cube[3] + cube[4] - cube[5] - cube[6] + cube[7];
        var dz = cube[0] + cube[1] + cube[2] + cube[3] - cube[4] - cube[5] - cube[6] - cube[7];
        var n = BABYLON.Vector3.Normalize(new BABYLON.Vector3(dx, dy, dz));
        this.normals.push(n.x);
        this.normals.push(n.y);
        this.normals.push(n.z);
    }

    computeNormalMeanStddev() {
        // compute mean and stddev of normals
        let meannml = new BABYLON.Vector3(0, 0, 0);
        let meanpos = new BABYLON.Vector3(0, 0, 0);
        for (let i=0; i<this.normals.length; i+=12) {
            let x = this.normals[i];
            let y = this.normals[i+1];
            let z = this.normals[i+2];
            meannml.addInPlaceFromFloats(x, y, z);
        }
        meannml.normalize();
        // compute variance
        let variance = new BABYLON.Vector3(0, 0, 0);
        let n = 0;
        for (let i=3; i<this.normals.length; i+=12) {
            let x = this.normals[i];
            let y = this.normals[i+1];
            let z = this.normals[i+2];
            variance.addInPlaceFromFloats(
                (x - meannml.x) * (x - meannml.x),
                (y - meannml.y) * (y - meannml.y),
                (z - meannml.z) * (z - meannml.z)
            );
            n++;
        }
        variance.scaleInPlace(1/n);
        // compute average of points
        n = 0;
        for (let i=0; i<this.vertices.length; i+=12) {
            let x = this.vertices[i];
            let y = this.vertices[i+1];
            let z = this.vertices[i+2];
            meanpos.addInPlaceFromFloats(x, y, z);
            n++;
        }
        meanpos.scaleInPlace(1/n);
        meanpos.addInPlaceFromFloats(this.x, this.y, this.z);
        // compute dot product of mean and centroid
        let centroid = new BABYLON.Vector3(this.x + this.chunkWidth/2, this.y + this.chunkWidth/2, this.z + this.chunkWidth/2);
        centroid.normalize();
        let dot = centroid.x * meannml.x + centroid.y * meannml.y + centroid.z * meannml.z;
        let angle = Math.acos(dot);
        let varmag = variance.length();
        console.log('variance', varmag, 'angle', angle);
        return { varmag, angle, meanpos };
    }

    //marches the cube
    marchCube(x: number, y: number, z: number, size: number, cube: number[], config: number) {
        //if the config is outside the 0 or 255 which are both -1 it should return
        if (config == 0 || config == 255) {
            return;
        }

        var edgeIndex = 0;
        for (var i = 0; i < 5; i++) {
            for (var p = 0; p < 3; p++) {
                var ind = triangleTable[config][edgeIndex];

                //if it hits the end of a triangle data array in the table then its done with the triangle
                if (ind == -1) { return; }

                //this gets the current corner its configuring
                var vert1 = new BABYLON.Vector3(x, y, z).add(cornerTable[edgeIndexes[ind][0]]).multiply(new BABYLON.Vector3().setAll(size));
                var vert2 = new BABYLON.Vector3(x, y, z).add(cornerTable[edgeIndexes[ind][1]]).multiply(new BABYLON.Vector3().setAll(size));
                var vertPos;


                if (smoothTerrain == true) {
                    //smoother terrain
                    var vert1S = cube[edgeIndexes[ind][0]];
                    var vert2S = cube[edgeIndexes[ind][1]];

                    var diff = vert2S - vert1S;
                    var vect = this.cutOff;

                    diff = Math.max(this.cutOff, (vect - vert1S) / diff);

                    vertPos = vert1.add(vert2.subtract(vert1).multiply(new BABYLON.Vector3().setAll(diff)));
                } else {
                    //rough (blocky) terrain
                    vertPos = vert1.add(vert2).divide(new BABYLON.Vector3(2, 2, 2));
                }

                if (smoothShaded == false) {
                    //flat shaded
                    this.vertices.push(vertPos.x);
                    this.vertices.push(vertPos.y);
                    this.vertices.push(vertPos.z);
                    this.indices.push((this.vertices.length / 3) - 1);
                    this.triangles.push(this.vertices.length - 1);
                } else {
                    //smooth shaded
                    var key = vertPos.x + "" + vertPos.y + "" + vertPos.z;
                    if (this.vertexChecker[key] == undefined) {
                        this.vertexChecker[key] = this.vertices.length + 3;

                        this.vertices.push(vertPos.x);
                        this.vertices.push(vertPos.y);
                        this.vertices.push(vertPos.z);
                        //this.computeSmoothNormals(x, y, z);
                        this.computeCubeNormals(cube);

                        this.indices.push((this.vertices.length / 3) - 1);
                        this.triangles.push(this.vertices.length - 1);
                    } else {
                        var index = this.vertexChecker[key];
                        this.indices.push((index / 3) - 1);
                        this.triangles.push(index - 1);
                    }
                }

                edgeIndex++;
            }


            //flat normals
            if (smoothShaded == false) {
                var v1 = new BABYLON.Vector3(
                    this.vertices[this.vertices.length - 3],
                    this.vertices[this.vertices.length - 2],
                    this.vertices[this.vertices.length - 1]
                );
                var v2 = new BABYLON.Vector3(
                    this.vertices[this.vertices.length - 6],
                    this.vertices[this.vertices.length - 5],
                    this.vertices[this.vertices.length - 4]
                );
                var v3 = new BABYLON.Vector3(
                    this.vertices[this.vertices.length - 9],
                    this.vertices[this.vertices.length - 8],
                    this.vertices[this.vertices.length - 7]
                );

                this.computeFlatNormals(v1, v2, v3);
            }
        }
    }
}

